            <h2 class="text-center my-5 fs-1">Ortodonzia</h2>
            <div class="col-12 col-md-7">
                <p class="fs-5">
                    Il termine ortodonzia deriva dal greco antico e significa letteralmente diritta dentatura; il linguaggio scientifico ricorre spesso al termine “ortognatodonzia”, denominazione più estesa che indica corretti rapporti sia dentali che scheletrici. In realtà l’ortodonzia consente di ottenere benefici non limitati al corretto allineamento dei denti ma tesi anche a migliorare l’equilibrio estetico delle labbra e del volto, ad assicurare una maggiore efficacia della funzione masticatoria e a favorire la conservazione della salute dei denti e dei tessuti di sostegno.
                </p>
                <h5>Chi è l'ortodontista?</h5>
                <p class="fs-5">L’ortodontista è quel dentista che si occupa di intercettare problematiche legate alla non corretta crescita scheletrica delle ossa cranio-facciali nel bambino (es. bambini che hanno il mento sporgente) e di risolvere le problematiche di disallineamento dentale (denti storti) nei bambini e negli adulti.
                </p>
                <h5>A che età è consigliato fare la prima visita odontoiatrica?</h5>
                <p class="fs-5">A partire dai 3 anni, è sempre consigliabile, non solo per controllare un dentino storto, ma per valutare l’assetto generale della bocca e seguirne l’evoluzione, verificare che non vi siano carie, accertarsi che i denti vengano puliti bene. Occuparsi insomma a 360° della salute dei denti per prevenire problemi futuri. Dalla prima visita, si consiglia di effettuare visite periodiche di controllo ogni 6 mesi.</p>
